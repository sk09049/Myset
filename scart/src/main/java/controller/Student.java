package controller;



public class Student {
	private String user;
	private String pass;
	private long phone;
	
	public long getPhone() {
		return phone;
	}
	public void setPhone(long phone) {
		this.phone = phone;
	}

	
	
	
	public String getUser() {
		return user;
	}
	public String getPass() {
		return pass;
	}
	public void setUser(String user) {
		this.user = user;
	}
	public void setPass(String pass) {
		this.pass = pass;
	}

}
